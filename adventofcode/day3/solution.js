function loopThroughRoutes() {
    let routesArr = [[1, 1], [3, 1], [5, 1], [7, 1], [1, 2]]
    let finalCounter = 1;
    for (let j=0; j<5; j++) {
        let xDist = routesArr[j][0];
        let yDist = routesArr[j][1];
        
        finalCounter = finalCounter * returnTreeCount(xDist, yDist, inputArr);
        console.log('_______________________________________Final Counter: ' + finalCounter);

    }
    console.log('Final Final Counter: ' + finalCounter);
}


function returnTreeCount(xDist, yDist, arr) {
    counter = 0;
    console.log(xDist + '  ' + yDist);
    let loops = Math.floor(arr.length/yDist);
    for (let i=0; i<loops; i++) {
        y = i*yDist;
        x = (i*xDist)%31;
        console.log(arr[y]);
        character = arr[y].charAt(x);
        console.log('loops: ' + loops + ', x: ' + x + ', y: ' + y);
        if (character === '#') {
            counter++
        }

    }
    console.log(counter);
    return counter
}

